int jump(std::vector<int> &A) {
    int jumps = 1, n = A.size();
    if (n == 1) return 0;

    int end = A[0], max = -1;
    for (int i = 0; i < n; ++i) {
        if (i + A[i] > end && i + A[i] > max)
            max = i + A[i];
        max = std::min(max, n - 1);

        if (i == end) {
            if (i == n - 1) break;
            if (max == -1)
                return -1;

            end = max;
            max = -1;
            jumps++;
        }
    }

    return jumps;
}

